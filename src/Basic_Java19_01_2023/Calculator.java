package Basic_Java19_01_2023;

public class Calculator {

    public double sum(double x, double y) { return x + y;}
    public double sub(double x, double y) { return x- y ;}
    public double mult(double x, double y) { return x * y;}
    public double div(double x, double y) { return x / y;}
    public double euTOdollar(double summInEuro) { return summInEuro * 1.0879;}
    public int secondsTohours(int secondsEnter) { return secondsEnter / 3600;}

    public double circleArea(double diametr) { return diametr * diametr / 4 * 3.14;}

}
