package Questions;

public class Switch {

    public static void main(String[] args) {

      int var = 3;

        switch (var) {
            case 1:
                System.out.println("Hello");
                break;
            case 2:
                System.out.println("World");
                break;
            case 3:
                System.out.println("Happy");
            case 4:
                System.out.println("Programming");
            case 5:
                System.out.println("Thanks");
                break;
            default:
                System.out.println("Congratulations");
                break;
        }
    }
}
